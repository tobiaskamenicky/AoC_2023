    public static long Part1()
    {
        var input = _input.AsSpan();

        Span<Range> segmentRanges = stackalloc Range[10];
        Span<int> segments = stackalloc int[10];
        var totalMatches = 0L;
        foreach (var line in input.EnumerateLines())
        {
            var sIndex = line.IndexOf(' ');
            var segmentsPart = line[(sIndex + 1)..];
            var count = segmentsPart.Split(segmentRanges, ',');
            for (var i = 0; i < count; i++)
            {
                segments[i] = int.Parse(segmentsPart[segmentRanges[i]]);
            }
            var record = line[..sIndex];

            totalMatches += Matches(record, segments[..count]);
        }

        return totalMatches;
    }

    public static long Part2()
    {
        var input = _input.AsSpan();

        Span<Range> segmentRanges = stackalloc Range[50];
        Span<int> segments = stackalloc int[50];

        var totalMatches = 0L;
        foreach (var line in input.EnumerateLines())
        {
            var sIndex = line.IndexOf(' ');
            var segmentsPart = line[(sIndex + 1)..];
            var count = segmentsPart.Split(segmentRanges, ',');
            for (var i = 0; i < count; i++)
            {
                segments[i] = int.Parse(segmentsPart[segmentRanges[i]]);
            }
            for (var i = 0; i < 5; i++)
            {
                segments[..count].CopyTo(segments[(count * i)..]);
            }

            var record = string.Join('?', Enumerable.Repeat(line[..sIndex].ToString(), 5)).AsSpan();

            totalMatches += Matches(record, segments[..(count*5)]);
        }

        return totalMatches;
    }

    private static long Matches(ReadOnlySpan<char> record, ReadOnlySpan<int> segments) =>
        segments.Length switch
        {
            0 => record.IsEmpty || !record.Contains('#') ? 1 : 0,
            1 => OneMatch(record, segments[0]),
            _ => MoreMatches(record, segments)
        };

    private static long MoreMatches(ReadOnlySpan<char> record, ReadOnlySpan<int> segments)
    {
        long result = 0;
        var leftSegments = segments[..(segments.Length / 2)];
        var middleSegment = segments[segments.Length / 2];
        var rightSegments = segments[(segments.Length / 2 + 1)..];

        var start = Sum(leftSegments) + leftSegments.Length - 1;
        var slack = record.Length - Sum(segments) - segments.Length + 2;
        for (var i = 0; i < slack; i++, start++)
        {
            var afterSegment = start + middleSegment + 1;
            if (record[start] == '#' || (afterSegment < record.Length && record[afterSegment] == '#'))
            {
                continue;
            }

            var middle = OneMatch(record.Slice(start+1, middleSegment), middleSegment);
            if (middle == 0)
            {
                continue;
            }

            var left = Matches(record[..start], leftSegments);
            if (left == 0)
            {
                continue;
            }

            var right = 1L;
            if (afterSegment + 1 < record.Length)
            {
                right = Matches(record[(afterSegment+1)..], rightSegments);
            }

            result += left * right;
        }

        return result;
    }

    private static long OneMatch(ReadOnlySpan<char> pattern, int segment)
    {
        long result = 0;
        for (var i = 0; i <= pattern.Length - segment; i++)
        {
            if (pattern[..i].Contains('#')) break;

            var isMatch = !pattern[i..(i + segment)].Contains('.');
            isMatch &= !pattern[(i + segment)..].Contains('#');

            if (!isMatch) continue;

            result++;
        }

        return result;

    }

    private static int Sum(ReadOnlySpan<int> values)
    {
        var result = 0;
        foreach (var value in values)
        {
            result += value;
        }

        return result;
    }

